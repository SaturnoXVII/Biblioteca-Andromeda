
function toggleNovoAutor() {
    var div = document.getElementById('div_novo_autor');
    div.style.display = div.style.display === 'none' ? 'block' : 'none';
}

function toggleNovaEditora() {
    var div = document.getElementById('div_nova_editora');
    div.style.display = div.style.display === 'none' ? 'block' : 'none';
}
